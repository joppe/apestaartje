/**
 * A hash of parameters that are used as arguments when calling a service.
 * The key is the name of the argument, the value is the value of the argument.
 *
 * @interface ServiceParameterDictionaryInterface
 */
interface ServiceParameterDictionaryInterface {
    [id:string]:any;
}
